package com.swp_group03.vaccination.vaccination_schedule_children_tracking_project.entity;

/**
 * Enum representing the various payment methods available for transactions
 */
public enum PaymentMethodType {
    /**
     * Payment made using a credit card
     */
    CREDIT_CARD,
    
    /**
     * Payment made using a debit card
     */
    DEBIT_CARD,
    
    /**
     * Payment made through a bank transfer
     */
    BANK_TRANSFER,
    
    /**
     * Payment made using an electronic wallet service
     */
    E_WALLET,
    
    /**
     * Payment made in cash
     */
    CASH,
    
    /**
     * Payment made using mobile payment services
     */
    MOBILE_PAYMENT,
    
    /**
     * Payment made using QR code
     */
    QR_CODE,
    
    /**
     * Payment made using insurance coverage
     */
    INSURANCE,
    
    /**
     * Payment made using a voucher or coupon
     */
    VOUCHER,
    
    /**
     * Other payment methods not listed above
     */
    OTHER,
    
    /**
     * Payment made using Mobile Money
     */
    MOMO,
    
    /**
     * Payment made using Visa card
     */
    VISA
} 